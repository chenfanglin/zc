package com.zc.model;

public class ConfigManager {

	private String labelName;

	private String labelID;

	private String labelTableID;

	private String event;

	private String url;

	private String enumTypeID;

	/**
	 * @return the labelName
	 */
	public String getLabelName() {
		return labelName;
	}

	/**
	 * @param labelName
	 *            the labelName to set
	 */
	public void setLabelName(String labelName) {
		this.labelName = labelName;
	}

	/**
	 * @return the labelID
	 */
	public String getLabelID() {
		return labelID;
	}

	/**
	 * @param labelID
	 *            the labelID to set
	 */
	public void setLabelID(String labelID) {
		this.labelID = labelID;
	}

	/**
	 * @return the labelTableID
	 */
	public String getLabelTableID() {
		return labelTableID;
	}

	/**
	 * @param labelTableID
	 *            the labelTableID to set
	 */
	public void setLabelTableID(String labelTableID) {
		this.labelTableID = labelTableID;
	}

	/**
	 * @return the event
	 */
	public String getEvent() {
		return event;
	}

	/**
	 * @param event
	 *            the event to set
	 */
	public void setEvent(String event) {
		this.event = event;
	}

	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * @param url
	 *            the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}

	/**
	 * @return the enumTypeID
	 */
	public String getEnumTypeID() {
		return enumTypeID;
	}

	/**
	 * @param enumTypeID
	 *            the enumTypeID to set
	 */
	public void setEnumTypeID(String enumTypeID) {
		this.enumTypeID = enumTypeID;
	}

}
